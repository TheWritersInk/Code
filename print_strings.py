phrase = "The Writer's Ink"

print(phrase) # Print predifined phrase
print(phrase.replace("W", "G")) # Replace W with G
print(phrase + " is cool") # concatation
print(phrase.lower()) # change to lowercase
print(phrase.upper()) # Change to uppercase
print(phrase.isupper()) # Check if it's uppercase
print(phrase.upper().isupper()) # Convert to upper and confirm
print(len(phrase)) # How many characters
print(phrase[0]) # String indexing starts with zero
print(phrase.index("W")) # Passing a parameter
print(phrase.index("jogging")) # Not found error



